# Feature extraction — what makes the setup bar different from noise?
# ---------------------------------------------------------------------------
def extract_features(launch, thesis_row=None, em_1sd=None):
    """Compute alertable features at the setup bar."""
    ts, o, h, l, c, v = launch["setup_bar"]
    prev1 = launch["prev1_bar"]
    prev2 = launch["prev2_bar"]
    bars  = launch["bars_ref"]
    i     = launch["i"]

    f = {}
    # --- bar shape ---
    rng = h - l
    body = abs(c - o)
    f["body_pct_of_range"] = (body / rng) if rng > 1e-6 else 0.0
    f["bar_is_bull"] = 1 if c > o else (-1 if c < o else 0)
    upper_wick = h - max(c, o)
    lower_wick = min(c, o) - l
    f["upper_wick_pct"] = (upper_wick / rng) if rng > 1e-6 else 0.0
    f["lower_wick_pct"] = (lower_wick / rng) if rng > 1e-6 else 0.0

    # --- volume anomaly vs recent 20 bars ---
    lookback = bars[max(0, i-20):i]
    vols = [b[5] for b in lookback if b[5] > 0]
    if vols:
        mean_v = sum(vols) / len(vols)
        f["vol_ratio_20"] = v / mean_v if mean_v > 0 else 1.0
    else:
        f["vol_ratio_20"] = 1.0

    # --- range anomaly vs recent 20 bars ---
    ranges = [(b[2] - b[3]) for b in lookback if (b[2] - b[3]) > 0]
    if ranges:
        mean_r = sum(ranges) / len(ranges)
        f["range_ratio_20"] = rng / mean_r if mean_r > 0 else 1.0
    else:
        f["range_ratio_20"] = 1.0

    # --- break of prior range ---
    def max_high_last_n(n):
        seg = bars[max(0, i-n):i]
        return max((b[2] for b in seg), default=h)
    def min_low_last_n(n):
        seg = bars[max(0, i-n):i]
        return min((b[3] for b in seg), default=l)

    f["break_high_5"]  = 1 if c > max_high_last_n(5)  else 0
    f["break_high_15"] = 1 if c > max_high_last_n(15) else 0
    f["break_high_30"] = 1 if c > max_high_last_n(30) else 0
    f["break_low_5"]   = 1 if c < min_low_last_n(5)   else 0
    f["break_low_15"]  = 1 if c < min_low_last_n(15)  else 0
    f["break_low_30"]  = 1 if c < min_low_last_n(30)  else 0

    # --- prior drift (was price already moving?) ---
    def drift_over(n):
        if i < n: return None
        return c - bars[i-n][4]
    f["drift_5m"]  = drift_over(5)
    f["drift_15m"] = drift_over(15)

    # --- time of day (minutes since 08:30 CT open) ---
    session_open = ts.replace(hour=8, minute=30, second=0, microsecond=0)
    f["min_since_open"] = int((ts - session_open).total_seconds() // 60)

    # --- VWAP alignment (session-to-bar) ---
    # Compute session VWAP up to and including the setup bar
    day_bars = [b for b in bars[:i+1] if b[0].date() == ts.date()]
    num_vwap = sum(((b[2]+b[3]+b[4])/3.0) * b[5] for b in day_bars)
    den_vwap = sum(b[5] for b in day_bars)
    vwap = (num_vwap / den_vwap) if den_vwap > 0 else c
    f["vwap"] = vwap
    f["above_vwap"] = 1 if c > vwap else (-1 if c < vwap else 0)
    f["vwap_dist"] = c - vwap

    # --- thesis-level context (if available) ---
    if thesis_row:
        gf = num(thesis_row.get("gamma_flip"))
        cw = num(thesis_row.get("call_wall"))
        pw = num(thesis_row.get("put_wall"))
        pv = num(thesis_row.get("pivot"))
        vp = num(thesis_row.get("vpoc"))
        f["v4_regime"] = (thesis_row.get("v4_regime") or "").strip()
        f["v4_conf_label"] = (thesis_row.get("v4_conf_label") or "").strip()
        try: f["bias_score"] = int(thesis_row.get("bias_score") or 0)
        except: f["bias_score"] = 0
        f["cagf_direction"] = (thesis_row.get("cagf_direction") or "").strip()
        f["cagf_regime"] = (thesis_row.get("cagf_regime") or "").strip()
        try: f["structure_confluence"] = int(thesis_row.get("structure_confluence") or 0)
        except: f["structure_confluence"] = 0
        # distances, normalized by em_1sd if available
        def dist(val):
            if val is None: return None
            d = c - val
            return (d / em_1sd) if em_1sd and em_1sd > 0 else d
        f["dist_gamma_flip_em"] = dist(gf)
        f["dist_call_wall_em"]  = dist(cw)
        f["dist_put_wall_em"]   = dist(pw)
        f["dist_pivot_em"]      = dist(pv)
        f["dist_vpoc_em"]       = dist(vp)
    return f

