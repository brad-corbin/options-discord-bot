# Thesis lookup — for each bar ticker+date, pick the best-matching thesis row
# ---------------------------------------------------------------------------
def best_thesis(thesis_by_ticker_date, ticker, date_str, ts):
    """Pick the best thesis row for this ticker/date given the current timestamp."""
    rows = thesis_by_ticker_date.get((ticker, date_str), [])
    if not rows:
        return None
    # Priority: silent > manual > morning > afternoon > session
    # BUT if timestamp is after 12:00 and afternoon exists, afternoon wins
    priority = {"silent": 0, "manual": 1, "morning": 2, "afternoon": 3, "session": 4}
    if ts.hour >= 12:
        # afternoon thesis (if present) takes precedence for afternoon bars
        priority = {"afternoon": 0, "silent": 1, "manual": 2, "morning": 3, "session": 4}
    rows = sorted(rows, key=lambda r: priority.get(r.get("session", ""), 99))
    return rows[0]

def recon_em(recon_by_ticker_date, ticker, date_str):
    """Pick a representative em_1sd for this ticker/date."""
    rows = recon_by_ticker_date.get((ticker, date_str), [])
    for r in rows:
        e = num(r.get("em_1sd"))
        if e: return e
    return None

