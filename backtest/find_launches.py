def find_launches_for_bars(bars, threshold=MOVE_THRESHOLD_DOLLARS, windows=WINDOWS_MIN):
    """
    Walk bars; for each one check whether the next W minutes produce a >= threshold
    favorable move before a comparable adverse move.
    Returns list of launch dicts.
    """
    launches = []
    i = 0
    n = len(bars)
    max_window = max(windows)
    while i < n - max_window - 1:
        # current bar is the last bar BEFORE the move — the "setup" bar
        # entry price assumed at close of this bar (realistic for alert-on-this-bar-then-enter-next)
        setup_close = bars[i][4]
        # Look forward by the smallest window first; take whichever window triggers
        triggered = False
        for W in windows:
            seg = bars[i+1:i+1+W]
            if len(seg) < W:
                break
            max_hi = max(b[2] for b in seg)
            min_lo = min(b[3] for b in seg)
            up_move = max_hi - setup_close
            dn_move = setup_close - min_lo
            # Also track WHEN each extreme happened to compute time-to-peak
            hi_idx = max(range(len(seg)), key=lambda k: seg[k][2])
            lo_idx = min(range(len(seg)), key=lambda k: seg[k][3])
            # Qualifier: favorable >= threshold AND adverse stays bounded
            if up_move >= threshold and hi_idx <= lo_idx + W:  # peak UP reached
                # Check that before the peak, adverse didn't exceed up_move * MAX_ADV_RATIO
                pre_peak = seg[:hi_idx+1]
                if pre_peak:
                    pre_min = min(b[3] for b in pre_peak)
                    pre_adv = setup_close - pre_min
                    if pre_adv <= up_move * MAX_ADV_RATIO:
                        launches.append(_pack_launch(bars, i, W, "UP", up_move, hi_idx + 1, pre_adv))
                        triggered = True
                        break
            if dn_move >= threshold and lo_idx <= hi_idx + W:  # peak DOWN reached
                pre_trough = seg[:lo_idx+1]
                if pre_trough:
                    pre_max = max(b[2] for b in pre_trough)
                    pre_adv = pre_max - setup_close
                    if pre_adv <= dn_move * MAX_ADV_RATIO:
                        launches.append(_pack_launch(bars, i, W, "DOWN", dn_move, lo_idx + 1, pre_adv))
                        triggered = True
                        break
        if triggered:
            i += DEDUP_SKIP_BARS
        else:
            i += 1
    return launches

def _pack_launch(bars, i, window, direction, magnitude, time_to_peak, pre_adverse):
    """Build a launch record including the setup bar + 2 bars of history."""
    setup = bars[i]
    prev1 = bars[i-1] if i >= 1 else None
    prev2 = bars[i-2] if i >= 2 else None
    return {
        "i": i, "ts": setup[0], "window": window, "direction": direction,
        "magnitude": magnitude, "minutes_to_peak": time_to_peak,
        "pre_adverse": pre_adverse,
        "setup_bar":  setup,  # (ts, o, h, l, c, v)
        "prev1_bar":  prev1,
        "prev2_bar":  prev2,
        "bars_ref":   bars,
    }

