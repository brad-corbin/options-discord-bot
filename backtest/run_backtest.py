def summ(values):
    """Return {n, mean, median, p25, p75, min, max} for a list of numbers."""
    v = [x for x in values if x is not None and not (isinstance(x, float) and math.isnan(x))]
    if not v:
        return {"n": 0, "mean": 0, "median": 0, "p25": 0, "p75": 0, "min": 0, "max": 0}
    v_sorted = sorted(v)
    n = len(v)
    def pct(p):
        k = max(0, min(n - 1, int(round(p * (n - 1)))))
        return v_sorted[k]
    return {
        "n": n,
        "mean": sum(v) / n,
        "median": statistics.median(v),
        "p25": pct(0.25),
        "p75": pct(0.75),
        "min": v_sorted[0],
        "max": v_sorted[-1],
    }

def fmt_summary_row(label, s, fmt="{:>8.2f}"):
    if s["n"] == 0:
        return f"  {label:<24} (no data)"
    parts = [
        f"n={s['n']:>3}",
        f"mean=" + fmt.format(s["mean"]),
        f"med="  + fmt.format(s["median"]),
        f"p25="  + fmt.format(s["p25"]),
        f"p75="  + fmt.format(s["p75"]),
        f"min="  + fmt.format(s["min"]),
        f"max="  + fmt.format(s["max"]),
    ]
    return f"  {label:<24} " + "  ".join(parts)

