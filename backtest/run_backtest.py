def slice_session(bars, session):
    """Return bars within the session's CT window."""
    start_t, end_t = SESSION_WINDOWS_CT.get(session, SESSION_WINDOWS_CT["session"])
    out = []
    for ts, o, h, l, c in bars:
        if ts.time() >= start_t and ts.time() <= end_t:
            out.append((ts, o, h, l, c))
    return out

def compute_directional_metrics(bars, session, dir_sign, em_1sd):
    """
    Compute MFE, MAE, time-to-peak, and simulated ATM 0DTE option P&L.
    dir_sign: +1 = long call (bull), -1 = long put (bear)
    Returns dict or None if insufficient bars.
    """
    seg = slice_session(bars, session)
    if len(seg) < 2:
        return None
    entry_ts, entry_open, _, _, _ = seg[0]
    entry_spot = entry_open
    if em_1sd is None or em_1sd <= 0:
        em_1sd = max(0.005 * entry_spot, 0.01)  # fallback ~0.5% daily vol

    premium = 0.4 * em_1sd  # Brenner-Subrahmanyam ATM approximation
    # Track signed favorable excursion per bar (using high/low, pick whichever is worse-case for each metric).
    mfe = -1e18
    mae = -1e18
    mfe_min = 0
    mae_min = 0
    peak_option_pnl_pct = -1.0  # option can't lose more than 100%
    trough_option_pnl_pct = 0.0
    final_spot = seg[-1][4]
    total_elapsed = int((seg[-1][0] - entry_ts).total_seconds() // 60)

    for (ts, o, h, l, c) in seg:
        elapsed = int((ts - entry_ts).total_seconds() // 60)
        # Favorable extreme bar level (for a call: the high; for a put: the low)
        fav_bar_spot = h if dir_sign > 0 else l
        adv_bar_spot = l if dir_sign > 0 else h
        fav_dS = dir_sign * (fav_bar_spot - entry_spot)
        adv_dS = dir_sign * (adv_bar_spot - entry_spot)  # this is signed; the most-adverse is the minimum

        if fav_dS > mfe:
            mfe = fav_dS
            mfe_min = elapsed
        if -adv_dS > mae:  # mae tracked as positive "loss" magnitude
            mae = -adv_dS
            mae_min = elapsed

        # Option P&L snapshot at the bar's extremes
        # Favorable price: compute option at fav_bar_spot
        opt_fav = option_price(entry_spot, fav_bar_spot, dir_sign, em_1sd, elapsed, TOTAL_SESSION_MIN)
        opt_adv = option_price(entry_spot, adv_bar_spot, dir_sign, em_1sd, elapsed, TOTAL_SESSION_MIN)
        pnl_fav = (opt_fav - premium) / premium
        pnl_adv = (opt_adv - premium) / premium
        if pnl_fav > peak_option_pnl_pct:
            peak_option_pnl_pct = pnl_fav
        if pnl_adv < trough_option_pnl_pct:
            trough_option_pnl_pct = pnl_adv

    final_dS = dir_sign * (final_spot - entry_spot)
    final_opt = option_price(entry_spot, final_spot, dir_sign, em_1sd, total_elapsed, TOTAL_SESSION_MIN)
    final_opt_pnl_pct = (final_opt - premium) / premium

    # MFE/MAE ratio (cap denominator to avoid div-by-zero; floor mae at 0.01)
    mae_for_ratio = max(mae, 0.01)
    ratio = mfe / mae_for_ratio if mfe > 0 else 0.0

    return {
        "entry_ts": entry_ts,
        "entry_spot": entry_spot,
        "final_spot": final_spot,
        "final_dS": final_dS,
        "mfe_dollars": mfe,
        "mae_dollars": mae,
        "mfe_min": mfe_min,
        "mae_min": mae_min,
        "mfe_mae_ratio": ratio,
        "mfe_over_em": mfe / em_1sd if em_1sd > 0 else 0.0,
        "mae_over_em": mae / em_1sd if em_1sd > 0 else 0.0,
        "premium_entry": premium,
        "peak_opt_pnl_pct": peak_option_pnl_pct,
        "trough_opt_pnl_pct": trough_option_pnl_pct,
        "final_opt_pnl_pct": final_opt_pnl_pct,
        "total_elapsed_min": total_elapsed,
        "bar_count": len(seg),
    }

def option_price(entry_spot, bar_spot, dir_sign, em_1sd, elapsed_min, total_min):
    """
    Simplified ATM 0DTE option pricing:
      option = intrinsic + bell-curve-time-value
      time_value = 0.4*em_1sd * sqrt(t_left/total) * exp(-dS^2 / (2*em_1sd^2))
    dir_sign: +1 for long call, -1 for long put.
    """
    strike = entry_spot
    if dir_sign > 0:
        intrinsic = max(bar_spot - strike, 0.0)
    else:
        intrinsic = max(strike - bar_spot, 0.0)
    t_left_frac = max(0.0, (total_min - elapsed_min) / total_min)
    atm_tv_now = 0.4 * em_1sd * math.sqrt(t_left_frac)
    dS_abs_sq = (bar_spot - strike) ** 2
    if em_1sd > 0:
        tv = atm_tv_now * math.exp(-dS_abs_sq / (2.0 * em_1sd * em_1sd))
    else:
        tv = 0.0
    return intrinsic + tv

def compute_pin_metrics(bars, session, em_1sd):
    """
    For PIN (neutral): measure peak |excursion| relative to em_1sd,
    closing excursion, and whether price breached 1σ intraday.
    """
    seg = slice_session(bars, session)
    if len(seg) < 2:
        return None
    entry_ts, entry_open, _, _, _ = seg[0]
    entry_spot = entry_open
    if em_1sd is None or em_1sd <= 0:
        em_1sd = max(0.005 * entry_spot, 0.01)

    peak_up = 0.0
    peak_dn = 0.0
    peak_up_min = 0
    peak_dn_min = 0
    first_breach_min = None
    for (ts, o, h, l, c) in seg:
        elapsed = int((ts - entry_ts).total_seconds() // 60)
        up = h - entry_spot
        dn = entry_spot - l
        if up > peak_up:
            peak_up = up
            peak_up_min = elapsed
        if dn > peak_dn:
            peak_dn = dn
            peak_dn_min = elapsed
        if first_breach_min is None and max(up, dn) > em_1sd:
            first_breach_min = elapsed

    final_spot = seg[-1][4]
    final_excursion = abs(final_spot - entry_spot)
    peak_excursion = max(peak_up, peak_dn)
    closed_inside = final_excursion <= em_1sd
    breached_intraday = first_breach_min is not None
    total_elapsed = int((seg[-1][0] - entry_ts).total_seconds() // 60)

    return {
        "entry_ts": entry_ts,
        "entry_spot": entry_spot,
        "final_spot": final_spot,
        "peak_up": peak_up,
        "peak_dn": peak_dn,
        "peak_excursion": peak_excursion,
        "peak_over_em": peak_excursion / em_1sd if em_1sd > 0 else 0.0,
        "final_excursion": final_excursion,
        "final_over_em": final_excursion / em_1sd if em_1sd > 0 else 0.0,
        "peak_up_min": peak_up_min,
        "peak_dn_min": peak_dn_min,
        "first_breach_min": first_breach_min,
        "breached_intraday": breached_intraday,
        "closed_inside": closed_inside,
        "total_elapsed_min": total_elapsed,
        "bar_count": len(seg),
    }

