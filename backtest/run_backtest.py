def parse_ts_ct(s):
    # Accept "YYYY-MM-DD HH:MM:SS", "YYYY-MM-DDTHH:MM:SS", with optional fractional seconds.
    s = s.strip()
    if "T" in s:
        s = s.replace("T", " ")
    # Strip any timezone suffix
    if "+" in s:
        s = s.split("+", 1)[0]
    if s.endswith("Z"):
        s = s[:-1]
    # Split off fractional seconds if present
    if "." in s:
        s = s.split(".", 1)[0]
    return datetime.strptime(s, "%Y-%m-%d %H:%M:%S")

def load_bars_for(ticker, date_str):
    path = os.path.join(BARS_DIR, f"{ticker}_{date_str}.csv")
    if not os.path.isfile(path):
        # Try nested dir (tarball sometimes keeps a prefix)
        for p in glob.glob(os.path.join(BARS_DIR, "**", f"{ticker}_{date_str}.csv"), recursive=True):
            path = p
            break
        else:
            return None
    bars = []
    try:
        with open(path) as f:
            reader = csv.DictReader(f)
            for row in reader:
                try:
                    ts = parse_ts_ct(row["ts_ct"])
                    o = float(row["open"])
                    h = float(row["high"])
                    l = float(row["low"])
                    c = float(row["close"])
                except (KeyError, ValueError):
                    continue
                bars.append((ts, o, h, l, c))
    except Exception as e:
        print(f"[bars] error reading {path}: {e}")
        return None
    bars.sort(key=lambda x: x[0])
    return bars

