# Filter definitions (must match the thresholds agreed in the prior chat).
def is_mega(rec_row, th_row):
    try:
        bias = float(rec_row["bias"])
    except (TypeError, ValueError):
        return False
    return (
        abs(bias) >= 5
        and (rec_row.get("prim_trig_correct") or "").strip().upper() == "TRUE"
        and (th_row.get("v4_regime") or "").strip() == "STRONG TREND / EXPLOSIVE"
    )

def is_trap(rec_row, th_row):
    try:
        bias = float(rec_row["bias"])
    except (TypeError, ValueError):
        return False
    return (
        abs(bias) >= 5
        and (rec_row.get("prim_trig_correct") or "").strip().upper() == "FALSE"
        and (th_row.get("v4_regime") or "").strip() == "STRONG TREND / EXPLOSIVE"
    )

def is_pin(rec_row, th_row):
    return (
        (th_row.get("v4_regime") or "").strip() == "STRONG PIN"
        and (rec_row.get("pred_dir") or "").strip().lower() == "neutral"
    )

